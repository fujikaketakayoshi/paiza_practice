<?php
/*配列 [2, 7, 1, 8, 2, 8] に対して、以下のクエリを処理せよ。
各クエリは区間 [l, r] を与え、該当区間の合計を出力する。

クエリ1: [1, 3] → 7 + 1 + 8 = 16  
クエリ2: [0, 2] → 2 + 7 + 1 = 10  
クエリ3: [4, 5] → 2 + 8 = 10
*/
$data = [2, 7, 1, 8, 2, 8];
$n = count($data);

$acc = array_fill(0, $n + 1, 0);

for ($i = 0; $i < $n; $i++) {
    $acc[$i+1] = $acc[$i] + $data[$i];
}

echo $acc[4] - $acc[1] . "\n";
echo $acc[3] - $acc[0] . "\n";
echo $acc[6] - $acc[4] . "\n";
